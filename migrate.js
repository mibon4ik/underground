const DATA_DIR = path.join(__dirname, 'data');

async function migrate() {
    await fs.ensureDir(DATA_DIR);

    // Basic keys
    const keys = ['theme', 'company', 'product', 'sales', 'crm', 'objections', 'customPages'];
    for (const key of keys) {
        const filename = key === 'customPages' ? 'pages' : key;
        await fs.writeJson(path.join(DATA_DIR, `${filename}.json`), INITIAL_DATA[key], { spaces: 2 });
    }

    // Auth
    await fs.writeJson(path.join(DATA_DIR, 'users.json'), [{ login: 'admin', password: 'uf2024' }], { spaces: 2 });

    // Placeholder for 15 features
    const features = ['prices', 'coaches', 'branches', 'leads', 'calculator', 'schedule', 'news', 'gallery', 'reviews', 'workout_programs', 'shop', 'booking', 'referral', 'promos', 'faq'];
    for (const f of features) {
        if (!await fs.pathExists(path.join(DATA_DIR, `${f}.json`))) {
            await fs.writeJson(path.join(DATA_DIR, `${f}.json`), [], { spaces: 2 });
        }
    }

    console.log('Migration complete!');
}

migrate();
